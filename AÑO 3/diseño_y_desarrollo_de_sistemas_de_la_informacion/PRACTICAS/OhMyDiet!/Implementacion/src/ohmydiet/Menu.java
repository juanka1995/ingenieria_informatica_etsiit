    public void rellenarTablaConDietas() throws SQLException{
        String [] titulos = {"ID Dieta","Fecha consumo"};
        String [] registros = new String[2];
        
        String sql = "SELECT * FROM dieta";
        
        model = new DefaultTableModel(null, titulos);
        
        Conectar cc = new Conectar();
        Connection cn = cc.conexion();
        
        if(cn != null){
            if(!sesionIniciada){
                JOptionPane.showMessageDialog(null, "Conexión establecida correctamente.");
                sesionIniciada = true;
            }
            try{
                Statement st = cn.createStatement();
                ResultSet rs  = st.executeQuery(sql);

                while(rs.next()){
                registros[0]=rs.getString("id_dieta");
                registros[1]=rs.getString("fecha_consumo");
                model.addRow(registros);
                }

                jTable1.setModel(model);
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(null, "Error en la conexión.\n"+ex);
            }       
        }
        else{
            JOptionPane.showMessageDialog(null, "Error al establecer la conexión.");
        }
    }


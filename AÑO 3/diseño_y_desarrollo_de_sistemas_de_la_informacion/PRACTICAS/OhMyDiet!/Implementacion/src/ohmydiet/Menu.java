    private void bBorrarDietaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarDietaActionPerformed
        int reply = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar la dieta "+idDieta.toString()+"?");
        if(reply == JOptionPane.YES_OPTION){
            Conectar cc = new Conectar();
            Connection cn = null;
            try {
                cn = cc.conexion();
            } catch (SQLException ex) {
                Logger.getLogger(CrearDieta.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(cn != null){
                boolean todoOK = true;
                
                // Añadimos la nueva dieta a la base de datos
                PreparedStatement psEliminar = null;
                try {
                    psEliminar = cn.prepareStatement("DELETE FROM asignar WHERE id_dieta="+idDieta.toString());
                    psEliminar.executeUpdate();
                    
                    psEliminar = cn.prepareStatement("DELETE FROM dieta WHERE id_dieta="+idDieta.toString());
                    psEliminar.executeUpdate();
                    
                    limpiarTabla();
                    rellenarTablaConDietas();
                    
                    JOptionPane.showMessageDialog(null, "Dieta "+idDieta+" eliminada correctamente.");
                } catch (SQLException ex) {
                    Logger.getLogger(CrearDieta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Error al establecer la conexión.");
            }
        }
        else if(reply == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Operación cancelada.");
        }
    }//GEN-LAST:event_bBorrarDietaActionPerformed

